								<h1 class="article-title">Суперкомпьютер смоделировал работу всей сердечно-сосудистой системы человека</h1>
								<ul class="article-meta">
									<li><i class="fa fa-clock-o"></i>  Май 17, 2018</li>
									<li><i class="fa fa-comments"></i> 47</li>
								</ul>
								<p>Американские ученые из Университета Дьюка (Duke University) в Дареме разработали суперкомпьютер, который позволяет создавать трехмерную модель сосудистой системы человека, практически не отличающейся по показателям от живого человеческого организма. Специалисты при помощи специального программного обеспечения получили отображение каждой артерии и вены, минимальный диаметр которых составил 1 миллиметр. По словам сотрудников Университета Дьюка, они были удивлены полученной точностью: циркуляция крови в теле человека и жидкости в модели сердечно-сосудистой системы человека, созданной на 3D-принтере, практически не отличались.</p>
								<p>Руководитель исследования Аманда Рендлс (Amanda Randles) отметила, что ученые впервые смогли создать реплику такой сложной системы человеческого организма. Её структура настолько хорошо проработана, что при прохождении жидкости по пластиковым сосудам производится даже имитация пульсации при перекачивании крови сердцем. Ученые назвали предложенную модель сердечно-сосудистой системы «Гарвей» в честь великого английского медика 17 века, основоположника физиологии и эмбриологии Уильяма Гарвея, который первым сформулировал и доказал теорию кровообращения (прим. – до этого ученые полагали, что кровь образуется в печени из пищи и движется по венам, слепо заканчивающимся в органах). </p> 
								<p>В основе «Гарвея» лежат данные компьютерной томографии и магнитно-резонансной томографии одного из пациентов. Сам процесс моделирования происходил в Ливерморской Национальной лаборатории в Калифорнии на суперкомпьютере, обладающим 1,6 млн процессоров. Он входит в десятку самых мощных суперкомпьютеров в мире, обладая производительностью 17,17 петафлопса. Согласно полученной информации от Аманды Рендлс, в будущем ученые из Университета Дьюка займутся следующим этапом проекта: проверкой того, как те или иные хирургические вмешательства, применяемые при различных сердечно-сосудистых заболеваниях, влияют на систему кровообращения в целом, например, новые модификации стентов в ангиохирургии</p> 
								
